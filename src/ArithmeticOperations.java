public class ArithmeticOperations {

	public static void main(String[] args) {

		int num1 = 3;
		int num2 = 10;


		int result = num1 + num2;

		System.out.println("num1 + num2 = " + result);


		result = num1 - num2;

		System.out.println("num1 - num2 = " + result);


		result = num1 * num2;

		System.out.println("num1 * num2 = " + result);


		result = num1 / num2;

		System.out.println("num1 / num2 = " + result);


		result = num1 % num2;

		System.out.println("num1 % num2 = " + result);
	}

}